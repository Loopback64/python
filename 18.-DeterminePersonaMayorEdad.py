#Determine si una persona es mayor de edad
"""Escriba un programa que permita el ingreso de un número entero, que simbolice la edad de
una persona, muestre a continuación sólo si es mayor de edad.

"""
n = int(input("Ingrese su edad:  "))
if (n>=18): 
    print("Es mayor de edad")

else: 
    print("Es menor de edad")


    




